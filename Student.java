public class Student    // Student class 
{
    public int roll;
    public String sname;
    
    public void setRoll(int roll){
        this.roll = roll;
    }
    
    public int getRoll(){
        return this.roll;
    }
    
    public void setName(String sname){
        this.sname = sname;
    }

    public String getName()
    {
        return this.sname;
    }
}